# Build prefix dict for fast lookup
_PREFIX_BY_LENGTH = {}
for prefix, vendor in _OUI_DATABASE.items():
    normalized = prefix.lower().replace('-', ':')
    length = len(normalized)
    if length not in _PREFIX_BY_LENGTH:
        _PREFIX_BY_LENGTH[length] = {}
    _PREFIX_BY_LENGTH[length][normalized] = vendor


def lookup(mac):
    """Look up the manufacturer for a MAC address.

    Args:
        mac: MAC address string (e.g. "d0:11:e5:f1:09:57")

    Returns:
        Vendor name string, or "Unknown" if not found.
    """
    if not mac:
        return "Unknown"

    normalized = mac.lower().replace('-', ':').strip()

    # Try to match OUI (first 3 bytes = 8 chars including colons)
    # Try from longest prefix first
    for length in sorted(_PREFIX_BY_LENGTH.keys(), reverse=True):
        prefix = normalized[:length]
        if prefix in _PREFIX_BY_LENGTH[length]:
            return _PREFIX_BY_LENGTH[length][prefix]

    return "Unknown"


def is_random_mac(mac):
    """Detect if a MAC address is a randomized/private address.

    iOS/iPadOS/macOS and Android use randomized MACs for privacy.
    """
    if not mac:
        return False
    normalized = mac.lower().replace('-', ':')
    parts = normalized.split(':')
    if len(parts) < 2:
        return False
    # Locally administered bit (second hex digit of first byte is 2,6,A,E)
    first_byte = int(parts[0], 16)
    return bool(first_byte & 0x02)  # bit 1 (0-indexed) = locally administered
